              <center><h1><u>Private Cloud for College to Create a Virtual Computing Lab</u></h1></center>
              
              <p>North Carolina State University needed to create a Virtual Computing Lab (VCL) so that the researchers and students of the university could have access to the computers and various lab facilities from anywhere any time. The University required a private cloud for creating the VCL because of the integrity , privacy, and other security concerns regarding the internal operations of the university.</p>

              <p>In 2004, the university launched its VCL, which was based on a private IaaS cloud. Researchers and students can now access the computers and other lab facilities without having to visit the university physically. VCL allows the Information Technology (IT) staff of the lab to simplify IT-support operations and reduce costs by consolidating computer labs through the private IaaS cloud. At present, there are 2000 IBM blade servers, VMware, open-source KVM software, and storage hardware components in the universityâ€™s VCL.</p>

              <p>The cloud software developed by the university includes three components, namely, a Web application that combines a Web interface and scheduler; a database; and a management node. The web interface and scheduler permit computer time and self-provision services to be reserved by users for themselves. The database is used to store all kinds of information that users want to save, and the management node is used for processing and controlling the operations.</p>

              <p>The Web interface is used by students to launch a virtual computer on their own systems or devices. They can utilize all the services they need through the virtual computer.Using the Web interface , students can also develop and/or test their applications on virtual servers.In addition, they have a facility to delete, refresh, and reload Virtual Machine Images (VMIs), in case something goes wrong.</p>

              <p>When it comes to the use of the Web interface by the researchers, they use it to provide and manage the VCL. In addition VCL also allows the university to cut infrastructure costs incurred on availing seats in computer labs and lots of other resources for the students and researchers.

