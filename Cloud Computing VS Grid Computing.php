       <p style="color:green">Note: The concept of grid computing was similar to the
         concept of electricity grid, where users could connect and use the power at any
         time. The amount of power used was measured using an electric meter.</p>

         <p>Cloud computing harnesses idle computer power over a network connection.
           Users have a metered utitily service to keep a track of the amount of utilization
           and generate a periodic bill. Like an electric or computer grid, cloud computing
           has no upfront, implementation, or capital expenses for the user. The users only pay
           for the amount used.</p>

      <p style="color:green">Note: Cloud computing is a term used for technologies that provide
        compute and application services that do not require users to know the IT hardware
        infrastructure, physical location, and configurations of the systems that deliver the services.
        It specifies  a new provisioning and delivery mechanism for IT services with dynamic
        scalibility and virtualized pool of resources and has the potential to completely disrupt
        the traditional IT models and go-to-market techniques.</p>
         
              
              
              
              
              
              
              
              
              
