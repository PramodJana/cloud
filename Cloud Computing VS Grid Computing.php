     <center><h1><u>Cloud Computing vs Grid Computing</u></h1></center>

     <P>In the early 1990s,<b> Carl Kesselman </b>and<b> Ian Foster</b> formulated
       the concept of grid computing as a cluster of computer systems that where
       geographically distributed but worked together to perform a common task. In a
       grid, a cluster of loosely coupled computers work together to solve a single
       problem that involves massive amounts of numerical calculation and compute cycles.
       Grid computing uses grid-controlling software that divides the work into smaller
       pieces and assigns each piece to a pool of thousands of computers. The controlling
       unit later assembles the results to build the output. Grids are usually used to
       harness idle computer power</p>

