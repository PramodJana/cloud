              <center><h1><u>Cloud Computing vs Peer-to-Peer Architecture</u></h1></center>
     <p>A peer-to-peer architecture is a network of hosts in which resource sharing,
       processing, and communications control are completely decentralized. Each hosts
       acts as a server or provider of certain services. However, it relies on other
       nodes within the network for other services. All clients on the network are equal
       in terms of providing and using resources and users are authenticated by each
       individual workstation.</p>

     <p style="color:green">Note:Peer-to-peer architecture is easy and inexpensive to
       implement. However, unlike cloud computing, it is only practical for very small
       organizations because of the lack of central data storage and administration.</p>

     <p>There are important benefit-related dissimilarities between cloud and peer-to-peer
       deployments. Distinctive benefits of cloud computing are that it can be easily
       scaled to meet growth demands, enables access to any type of hosted applications,
       does not burden user-end devices, and needs to be  configured with the highest
       levels of security. On the other hand, peer-to-peer deployments are relatively in
       expensive and simple to set up and manage.</p>

     <p>The shortcomings with cloud computing include high initial capital investment and
       good technology expertise to establish and managed it. The downside of peer-to-peer
       architecture is that it is limited in extensibility, tends to overburden user
       workstations by making them work as servers for other users, has lax security,
       and is typically unable to provide any type of system-wide service. But, these
       downsides of peer-to-peer implementations can be overcome by using servers that
       are dedicated for computing, storage, management and monitoring.</p>




              
              
              
              
              
              
              
              
              
