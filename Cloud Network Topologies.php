              <center><h1><u>Cloud Network Topologies</u></h1></center>
              <p>Cloud network topology characterizes the manner in which consumers’ access public or private cloud resources over the internet or over corporate intranets. The cloud network can be viewed as comprising the following three components:</p>
              
              <ul>
                  <li>The front-end or user-access layer, which helps users initiate a connection to cloud services.</li>
                  <li>The compute layer, which comprises cloud servers, storage, load-balancers, and security devices.</li>
                  <li>The underlying network layer, which can be a Layer 2 or Layer 3 topology.</li>
              </ul>
              
              <p>A Layer 2 cloud topology is easier to implement and manage and is not location-dependent. It maintains IP and MAC addresses to ensure that all the servers and other devices have the same addresses and routing protocols. In this case, the cloud is a direct extension of the datacenter network. There is no need for users to re-architect their settings. They can run their applications in the cloud in a similar manner as they do for their other traditionally-hosted applications.</p>
              
              <p>The Layer 3 cloud network is used to transfer packets from a source host in a cloud to an application in another cloud. Each cloud is characterized to be a separate network having a definite set of IP addresses and characteristics.</p>
              
              
              <p style="color:green">NOTE: organizations adopting Layer 3 cloud services need to have internal applications that match the cloud providers’ requirement. This requires re-designing and re-configuring the corporate network to suit the requirements of the cloud provider, which is itself is a significant task.</p>
              
              
              <h2><u>Automation for cloud deployments</u></h2>
              <p>It is imperative for cloud providers to automate cloud operations. Automation for cloud is important for all cloud deployment, which are IaaS, SaaS and PaaS. For IaaS, automation must be used to implement centralized policies for various issues such as security, authorization to access resources, etc. It can be used to automatically allocate resources such as bandwidth, storage etc. based on real-time workload.</p>
              
              <p>For PaaS, automation must be used to build a highly flexible platform that provides real-time workload optimization, security, compliance, and metering of utilization during the entire lifecycle of an application.</p>
              
              <p>For SaaS, automation must be built-in to improve user experience with the applications, improve performance by dynamic resource allocation, and protect from internal malware.</p>
              
              <p style="color:green">NOTE: Automation has various benefits. It provides users with highly-controlled and source access to applications and resources and also facilities the self-service of user requests in a real time.</p>
              
              <p>Resource virtualization has proved to be advantageous for automating various aspects of cloud. It helps in following ways:</p>
              
              <ul>
                <li>Facilities on-demand and optimized allocation of resources such as bandwidth, storage, memory, etc. for virtual machines.</li>
                  <li>Creates new virtual machines if required</li>
                  <li>Replicates data and helps with disaster recovery</li>
                  <li>Switches applications to a new hardware infrastructure, in the event of hardware problems and thus improve service uptime</li>
                  <li>Tunes server or application parameters in real-time to improve performance</li>
                  <li>Implements security policies to protect against Internet threads</li>
                  <li>Abide by compliance and regulations issues</li>
                  <li>Provide Service Level Agreement (SLA) mandated quality of services</li>
                  <li>Saves power by automatically shutting down part of hardware infrastructure during low activity period</li>
              </ul>
              
              <p>Automation in a cloud is possible due to a few characteristics that differentiate it from traditional infrastructure. A cloud has a virtualized pool of resources such as memory, CPU, bandwidth, storage, etc. There is a pre-defined set of policies used to allocate resources from the pool to meet work-load in real-time. This helps in enhancing performance. To meet high uptime, it has an automated data backup and replication process, along with ability to automatically retrieve data that is lost or inaccessible due to hardware or network failure, data deletion or corruption.</p>
              
              
              
              
              
              
              
              
                            
              
              
