     <h2><u>Getting to know the cloud</u></h2>

     <p>The most common defination in use is the one by the National Institude
       of Standerds and Technology (NIST), USA, in there special publication
       800-145, which states, “Cloud Computing is a model for enabling convenient,
       on-demand network access to a shared pool of configurable computing resourses
       (e.g., networks, servers, storage, applications, and services) that can be
       rapidly provisioned and released with minimal management effort or service
       provideer interaction.”</p>

       <p>Cloud Computing has several definations. According to NIST, the five
         salient features of cloud are:</p>

         <p>
           <ul>
             <li><b>On-demand Self-Service</b> -A consumer can set up computing
              capabilities, such as server time and network storage, as needed,
             automatically without having any direct communication with each
             service provider.</li>
             <li><b>Broad Network Access</b> -Capabilities are available over
               the network and accessed through normal mechanisms that are used
               by various devices, such as mobile phones, tablets, laptops,
               or workstations.</li>
             <li><b>Resource Pooling</b> -The provider’s computing resourses,
               such as storage, processing, memory and network bandwidth, are
               pooled to serve multiple consumers by using a multi-tenant model.
               Further, on the basis of the consumer’s demand, various physical
               and virtual resourses are systematically assigned and re-assigned.
               There is a sense of location independence, in that the customer
               generally has no control or knowledge over the exact location of
               the provided resources but may well be able to specify the location
               at a higher level of abstraction, for example country, state, or
               datacenter.</li>
               <li><b>Rapid Elasticity</b> -Cloud computing capabilities can be
                 systematically provisioned to meet demand and load requirements.
                 To the consumer, the capabilities available for provisioning often
                 appers to be unlimited and can be appropriated in any quantity at
                 any time.</li>
               <li><b>Measured Services</b> -Cloud systems can automatically control
                   and optimize the use of resourses by leveraging a metering capability
                   at some level of abstraction that is appropriate for the type of service
                   (e.g., storage, processing, bandwidth, and active user accounts).
                   Resource usage can be monitored, controlled, and reported, providing
                   transparacy for both the provider and consumer.</li>
  
              </ul>        
