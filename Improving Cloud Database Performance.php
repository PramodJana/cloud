       <h1><center><u>Improving Cloud Database Performance</u></center></h1>       
              
              <p>Cloud databases offer noteworthy benefits over traditionally-hosted internal databases. Moreover, cloud vendors continue to add and improve their database offerings to make it a convincing option for enterprises. Cloud databases have higher ease of accessibility, better replication to remote datacenters alongside automation and better elasticity.</p>
              
              <p style="color:green">NOTE: Cloud databases have their share of woes, arising from inherent cloud problems, such as security, data privacy, multi-tenancy, low barrier to entry for malicious users, and reliance on a third-party provider for business-critical services.</p>
              
              <p><i>Sharding</i> a cloud database is another technique to improve performance. It is a process of splitting a large database into a number of smaller databases, each being hosted on a separate server. It helps to boost the performance of applications that require frequent and large database transactions.</p>
              
              
              <p><i>Sharding</i> also helps reduce the size of the database index, thus decreasing the time needed for searches within the database. To further improve performance and availability, providers offer a horizontally-scaled server environment, where it is quick and easy to bring up more virtual machines to meet higher workloads. Besides performance, providers focus upon improving database integrity by using database profilers.</p>
              
              <p><i>Sharding</i> analyses the source database for inconsistencies in index, table relationships, or data structure. By examining the data quality and utilization pattern, it is able to point out the potential problems, if any, within a database. This improves the performance of the database.</p>
              
              
              <h2>Cloud Service Brokerage (CSB)</h2>
              <p>A <i>Cloud Service Brokerage (CSB)</i> is an organization that plays a role as a facilitator or inter-mediator for delivering cloud services. The CSB is usually a telecommunication or datacenter hosting service provider with a large number of customers.</p>
              
              <p>In the cloud provider-consumer relationship, CSBs are an optimal entity involved with mediating between the two. However in the process, the CSBs also offer additional value to the provider and consumer.</p>
              
              <p>They help producers by relieving them of acquiring customers, billing, and enabling integrated access to multiple cloud services. The cloud consumers get integrated access to one or more cloud and value-added services such as cloud backups, SaaS, and Identity Management (IdM).</p>
              
