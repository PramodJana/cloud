                  </li>
                  <li><b>Risk:</b> Services in cloud has more risk compared to traditional system. The risk involvement is more due to multi –tenant environment supported by cloud .Multi-tenancy increases the risk since a single machine may be shared by the workloads of any combination of users.</li> 
 
 
                  <li><b>Location of workload:</b> Portability of workloads is their between nodes without any inconvenience to the clients. Mostly the cloud vendors take care of workload location but as a user for efficient resource management one should pay more attention to where the workload is hosted.</li>

