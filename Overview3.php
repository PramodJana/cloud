              <center><h1><u>Deployment Model</u></h1></center>
              
              <p><b>“The cloud has delivered scalable, elastic and cost-efficient computing to businesses of all sizes. It is a type of computing that mainly depends on resource sharing rather than handling of application. Taking the advantage of resource sharing, cloud computing is able to achieve consistency and economies of scale”.  </b></p>
              
              <p>The types of cloud computing are classified based on two models. Cloud computing service models and cloud computing deployment models. </p>
              
              <h2><u>Deployment Model:</u></h2>
              <p><b>The deployment models represent the cloud hosting environment which is mainly distinguished by the proprietorship, size, access criteria, architecture and SLA.  </b></p>
              
              <p><b>Deployment model generally focuses on- </b></p>
              
              <ul>
                  <li>Purpose of cloud service.</li>
                  <li>Structure of cloud.</li>
                  <li>The nature of   cloud.</li>
                  <li>Controls operating cost.</li>
                  <li>Workload characterization.</li>
                  <li>Nonfunctional business requirements.</li>
              </ul>
              
              <h2><u>Cloud Deployment Implications: </u></h2>
              <p>The deployment models have numerous implications. Following implications are needed to be considered irrespective of type of deployment model:- </p>
              
              <ul>
              <li><b>Network Dependency:</b> A reliable and secure network is highly desirable for good performance whether choice is off, on-site or off-shore.</li> 
 
                  <li><b>IT skills:</b> In order to manage various user devices that access cloud, resources with traditional IT skills are required, along with updation of existing resources.
                  <br><br>
						
