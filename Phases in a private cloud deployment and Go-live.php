              <center><h1><u>Phases in a Private Cloud deployment and Go-live</u></h1></center>
              <p>According to Gartner, a private cloud is one of the top interest areas among all cloud computing areas. 75% of respondents in Gartner polls say that they plan to persue private cloud testing and adoption by 2014. There are several phases involved in deployment of a private cloud, as shown in the figure below</p>
              <br>
			  <center>
               <img src="img1/phases%20in%20a%20private%20cloud%20and%20go%20live.jpg" class="img-fluid" alt="Responsive image" width="500px">
			   </center>
              <br><br>
              
              <p>Let us discuss these phases in brief through the following points:</p>
              <ul>
              <li><b>Phase 1-</b> Document Business Requirements-In this phase, it is important to work with all the internal, external, and interested stakeholders to understand their needs from the private cloud. Study the existing IT configuration, services, and processes to see if the need is to move the existing service to the private cloud or deploy new services. Note the impact on existing users and whether any part of the user or IT support staff needs training</li>
              <li><b>Phase 2-</b> Virtualize the IT infrastructure –In this phase, it is important that servers,  networks and storage systems need to be virtualized to form a centralized resource pool, available for any user or applications. Common virtualization products are available from Microsoft, VMware, and Citrix.</li>
              <li><b>Phase 3-</b> Implement a Self-Service Portal- In this phase, the portal helps user to find a list of standard offerings within the private cloud. It should be an easy-to-use interface with a list of your offerings, prices, online ordering, and billing. It should have details on pricing options and SLAs for the hosted services</li>
              <li><b>Phase 4- </b>Automated Provisioning- This is a continuing exercise. You can start by automating a few services and offerings adding more later. You can initially offer one or two services and offer more after the user is comfortable. As much as possible, the user’s needs should be offered automatically from the catalog.</li>
              <li><b>Phase 5-</b> Accounting for Resource Utilization- The completion of this phase ensures that you have a system to automatically record the utilization of cloud resources such as CPU, memory, bandwidth, licenses, storage, etc.</li>
              <li><b>Phase 6-</b> Online Billing- In this phase, the providers should convert the consumption to a bill. However, providers should explain each line item in the bill. Also, providers should offer real-time resources utilization, so that users are aware of the consumption and expenses without waiting till the end of the billing cycle(typically a month)</li>
              </ul>
              
              
              
