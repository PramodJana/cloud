              <p>Enterprises usually take steps toward cloud computing with a private cloud. They maintain their own datacenters or obtain the hosting services from a third party vendor. They invest to procure, setup, and manage the private cloud. However, the resources may prove to be in adequate to meet demands during peak loads, which lead to a requirement for a hybrid model.</p>

                 <p> A hybrid cloud is a combination of a public and private cloud, wherein organizations can maintain vital information in the private cloud and migrate less critical information to the public cloud. Data portability must be a guaranteed factor when moving between public and private infrastructure.</p>
              
