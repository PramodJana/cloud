<li> Legal liability and compliance</li>
<li> Loss of control on all user data, as the application and infrastructure are
 hosted at a third-party location</li>
<li> Complexity in tracking operations</li>
<li> Unpredictable availability of services</li>
<li>Uneven performance and delayed response time to the end users
due to WAN-based network access</li>
<li>Inability to use services when disconnected from the Internet.</li>
