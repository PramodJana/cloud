              <center><h1><u>Prons And Cons of Cloud Computing</u></h1></center>

     <h3>Prons</h3>
       <ol>
         <li><b>Cost savings:</b> If you are worried about the price tag that would come
           with making the switch over to cloud computing, you aren't alone; 20
           percent of organizations are concerned about the initial cost of
           implementing a cloud-based server. But those who are attempting to weigh
           the advantages and disadvantages of using the cloud need to consider more
           factors than just initial price; they need to consider ROI.<br>
           Once you're on the cloud, easy access to your company's data will save
           time and money in project start-ups. And, for those who are worried that
           they'll end up paying for features that they neither need nor want, most
           cloud computing services are pay-as-you-go. This means that if you don't
           take advantage of what the cloud has to offer, then at least you won't have
           to be dropping money on it. <br>
           The pay-as-you-go system also applies to the data storage space needed to
           service your stakeholders and clients, which means that you'll get exactly
           as much space as you need, and not be charged for any space that you don't.
           Taken together, these factors result in lower costs and higher returns.
           Half of all CIOs and IT leaders surveyed by Bitglass reported cost savings
           in 2015 as a result of using cloud-based applications.</p></li>

           <li><b>Security:</b> One major hang up that many organizations have when it comes
             to adopting a cloud computing solution is the issue of security. After all,
             when files, programs, and other data aren't kept securely on site, how can
             you know that they are being protected? If you can remote access your data,
             then what's stopping some cyber criminal from doing the same thing? Well,
             quite a bit, actually. <br>
             For one thing, a cloud host's full-time job is to carefully monitor security,
             which is significantly more efficient than a conventional in-house system,
             where an organization must divide its efforts between a myriad of IT concerns,
             with security being only one of them. And while most businesses don't like to
             openly consider the possibility of internal data theft, the truth is that a
             staggeringly high percentage of data thefts occur internally and are perpetrated
             by employees. When this is the case, it can actually be much safer to keep sensitive
             information off-site. Of course, this is all very abstract, so let's consider
             some solid statistics. <br>
             RapidScale claims that 94 percent of businesses saw an improvement in security
             after switching to the cloud, and 91 percent said the cloud makes it easier to
             meet government compliance requirements. The key to this amped-up security is
             the encryption of data being transmitted over networks and stored in databases.
             By using encryption, information is less accessible by hackers or anyone not
             authorized to view your data. As an added security measure, with most cloud-based
             services, different security settings can be set based on the user.</li>

             <li><b>Flexibility: </b>Your business has only a finite amount of focus to divide between
               all of its responsibilities. If your current IT solutions are forcing you to
               commit too much of your attention to computer and data-storage issue, then you
               aren't going to be able to concentrate on reaching business goals and satisfying
               customers. On the other hand, by relying on an outside organization to take care
               of all IT hosting and infrastructure, you'll have more time to devote towards the
               aspects of your business that directly affect your bottom line.<br>
               The cloud offers businesses more flexibility overall versus hosting on a local
               server. And, if you need extra bandwidth, a cloud-based service can meet that
               demand instantly, rather than undergoing a complex (and expensive) update to
               your IT infrastructure. This improved freedom and flexibility can make a
               significant difference to the overall efficiency of your organization. A 65%
               majority of respondents to an InformationWeek survey said 'the ability to
               quickly meet business demands' was one of the most important reasons a business
               should move to a cloud environment.</li>

               <li><b>Mobility: </b>Cloud computing allows mobile access to corporate data via smartphones
                 and devices, which, considering over 2.6 billion smartphones are being used globally
                 today, is a great way to ensure that no one is ever left out of the loop. Staff with
                 busy schedules, or who live a long way away from the corporate office, can use this
                 feature to keep instantly up-to-date with clients and coworkers.<br>
                 Through the cloud, you can offer conveniently accessible information to sales staff
                 who travel, freelance employees, or remote employees, for better work-life balance.
                 Therefore, it's not surprising to see that organizations with employee satisfaction
                 listed as a priority are up to 24 percent more likely to expand cloud usage.</li>

                 <li><b>Insight:</b> As we move ever further into the digital age, it's becoming clearer
                    and clearer that the old adage 'Knowledge is power' has taken on the more modern
                    and accurate form: 'Data is money.' Hidden within the millions of bits of data
                    that surround your customer transactions and business process are nuggets of
                    invaluable, actionable information just waiting to be identified and acted upon.
                    Of course, sifting through that data to find these kernels can be very difficult'
                    unless you have access to the right cloud computing solutions.<br>
                    Many cloud-based storage solutions offer integrated cloud analytics for a bird's-eye
                    view of your data. With your information stored in the cloud, you can easily
                    implement tracking mechanisms and build customized reports to analyze information
                    organization-wide. From those insights, you can increase efficiencies and build
                    action plans to meet organizational goals. For example, the beverage company Sunny
                    Delight was able to increase profits by about $2 million a year and cut $195,000
                    in staffing coststhrough cloud-based business insights.</li>

          <li><b>Increased collaboration: </b>If your business has two employees or more, then you should
            be making collaboration a top priority. After all, there isn't much point to having a
            team if it is unable to work like a team. Cloud computing makes collaboration a simple
            process. Team members can view and share information easily and securely across a
            cloud-based platform. Some cloud-based services even provide collaborative social spaces
            to connect employees across your organization, therefore increasing interest and engagement.
            Collaboration may be possible without a cloud computing solution, but it will never be as easy,
            nor as effective.</li>

          <li><b>Quality control:</b> There are few things as detrimental to the success of a business as
            poor-quality, inconsistent reporting. In a cloud-based system, all documents are stored
             in one place and in a single format. With everyone accessing the same information,
             you can maintain consistency in data, avoid human error, and have a clear record of any
             revisions or updates. Conversely, managing information in silos can lead to employees
             accidentally saving different versions of documents, which leads to confusion
             and diluted data.</li>

          <li><b>Disaster recovery:</b> One of the factors that contributes to the success of a business
            is control. Unfortunately, no matter how in control your organization may be when it
            comes to its own processes, there will always be things that are completely out of
            your control, and in today's market, even a small amount of unproductive downtime can
            have a resoundingly negative effect. Downtime in your services leads to lost productivity,
            revenue, and brand reputation.<br>
            But while there may be no way for you to prevent or even anticipate the disasters that could
            potentially harm your organization, there is something you can do to help speed your
            recovery. Cloud-based services provide quick data recovery for all kinds of emergency
            scenarios' from natural disasters to power outages. While 20 percent of cloud users claim disaster
            recovery in four hours or less, only 9 percent of non-cloud users could claim the same.
            In a recent survey, 43 percent of IT executives said they plan to invest in or improve cloud-based
            disaster recovery solutions.</li>

          <li><b>Loss prevention:</b> If your organization isn't investing in a cloud-computing solution,
            then all of your valuable data is inseparably tied to the office computers it resides in.
            This may not seem like a problem, but the reality is that if your local hardware
            experiences a problem, you might end up permanently losing your data. This is a more
            common problem than you might realize; computers can malfunction for many reasons,
            from viral infections, to age-related hardware deterioration, to simple user error.
            Or, despite the best of intentions, they can be misplaced or stolen (over 10,000 laptops
            are reported lost every week at major airports). <br>
            If you aren't on the cloud, you're at risk of losing all the information you had saved locally.
            With a cloud-based server, however, all the information you've uploaded to the cloud remains
            safe and easily accessible from any computer with an internet connection, even if the computer
            you regularly use isn't working.</li>

          <li><b>Automatic software updates:</b> For those who have a lot to get done,
            there isn't anything more irritating than having to wait for system update to be installed.
            Cloud-based applications automatically refresh and update themselves, instead of forcing an
            IT department to perform a manual organization-wide update. This saves valuable IT staff
            time and money spent on outside IT consultation. PCWorld lists that 50 percent of cloud
            adopters cited requiring fewer internal IT resources as a cloud benefit.</li>

          <li><b>Competitive edge:</b> While cloud computing is increasing in popularity, there are still
            those who prefer to keep everything local. That's their choice, but doing so places them
            at a distinct disadvantage when competing with those who have the benefits of the cloud
            at their fingertips. If you implement a cloud-based solution before your competitors,
            you'll be farther along the learning curve by the time they catch up.
            A recent Verizon study showed that 77 percent of businesses feel cloud technology
            gives them a competitive advantage, and 16 percent believe this advantage is
            'significant.'</li>

          <li><b>Sustainability: </b>Given the current state of the environment, it's no longer enough
            for organizations to place a recycling bin in the breakroom and claim that they're doing
            their part to help the planet. Real sustainability requires solutions that address wastefulness
            at every level of a business. Hosting on the cloud is more environmentally-friendly, and results
            in less of a carbon footprint. <br>
            Cloud infrastructures support environmental proactivity, powering virtual services rather
            than physical products and hardware, and cutting down on paper waste, improving energy efficiency,
            and (given that it allows employees access from anywhere with an internet connection)
            reducing commuter-related emissions. A Pike Research report predicted data center energy consumption will
            drop by 31 percent from 2010 to 2020 based on the adoption of cloud computing and other virtual data
            options.</li>
</ol>
