              <h2>Risk Management:</h2>
              <p><b>The fundamental issues of governance and enterprise risk management in Cloud computing concern the identification and implementation of:</b></p>
              
              <ol>
              <li>Appropriate organizational structures.</li>
                  <li>Processes.</li>
                  <li>Controls to maintain effective information security governance, risk management, and compliance.</li>
              </ol>
              
              <p>Enterprise should also assure reasonable information security across the information supply chain, encompassing providers and customers of Cloud Computing services and their supporting third party vendors, in any cloud deployment model.</p>
              
              <h2>Risk management Framework by NIST:</h2>
              <p>Cloud-computing providers have encountered dangerous security risks and problems. These security risks would negatively affect confidentiality, privacy, reliability, and integrity of a provider’s services.</p>

                 <p> Therefore, a specific RMF process dealing with security risks and problems is recommended.</p>
                 <p> The basic idea of RMF is simply identify, rank, track, and understand software security risk as it changes over time. This framework can be used widely and flexibly because it can fit with small and large enterprises. Also, the advantage of using RMF is that it “is not specific to security risks; it can be applied in non-software situations”. However, the main goal of using RMF with cloud-computing providers is to consistently track and handle risks and threats.
              </p>
              <br>
              <center>
              <img src="img1/management%20framework.png" class="img-fluid" alt="Responsive image">
              </center>
              <br>
              
              <ul>
                  <li><b>Categorize</b> the information system and the information processed, stored, and transmitted based on an impact analysis.</li>
                  <li><b>Select</b> baseline security controls.</li>
                  <li><b>Implement </b>the security controls and describe how the controls are employed within the information system and its environment of operation.</li>
                  <li><b>Assess</b> the security controls using appropriate assessment procedures.</li>
                  <li><b>Authorization</b> refers to giving rights and privileges to asses.</li>
                  <li><b>Monitor</b> the security impact analyses of the changes and developing the report of the security state of the system to designated organizational officials.</li>
              </ul>
              
