              <h3>Disadvantage   <br>
               It‟s a „what you see is what you get‟ form of cloud computing. </h3>
              <ol type="i">
              <li>Dependent on browser capabilities.</li>
              <li>Security concern is too high.</li>
              <li>Less customizable, compare to cloud at the infrastructure and platform level.</li>
                  <ul>
                      <li>The customer has no control over the system processing its data.</li>
                      <li>There is no control over which customers use the software; 
                          the software is used by a large amount of users.</li>
                      <li>Little or no control over the parameters of deployed software.</li>
                      <li>Vendor specific i.e. difficult to integrate.</li> 
                      <li>Cloud provider has full access to customer’s data, unless cryptography is used.</li>
                  </ul>
                  
              </ol>
              
