              <center><u><h1>Software as a Service (SaaS)</h1></u></center>
              <p>SaaS is the capability to use the provider’s applications running on an cloud infrastructure. The applications are accessible from various client devices through a web browser. As in the case of PaaS, the customer does not manage or control the underlying cloud infrastructure (Servers, Storage, OS , networks) or the application features. The customer can however, configure user-specific application parameters and settings.</p>
