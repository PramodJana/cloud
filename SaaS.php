              <h3>SaaS Characteristics</h3>
              <ul>
              <li>For software delivery “one to many” model is used.</li>
              <li>Management of software is done from a centralized location.</li>
              <li>Access to application is via the internet.</li>
              <li>The end users need not worry about software patches and upgrades </li>
              <li>To enable integration with other applications, application programming interfaces (APIs) is used.</li>
              </ul>
              
              <h3>Benefits </h3>
              <ul>
              <li>Minimal planning and is easy to setup, as applications are usually simple in usage and readymade.</li>
              <li>Deployment is fast as the software is fully provisioned on demand.</li>
              <li>The customer does not need to worry about the management of applications, as this is all handled by the provider.</li> 
              <li>Dynamic Scalability</li>
              <li>Device independence.</li>
              <li>Cost effective Cloud service as you only lease the software and not the resource.</li>
              <li>Collaborative, it allows multiple sharing of information.</li>
              <li>Constant Updates</li>
              </ul>
              
              <br><br>
