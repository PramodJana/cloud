              <center><h1><u>Service Oriented Architecture and Utility Computing</u></h1></center>

<h3>Service oriented Architecture (SOA):-</h3>
<p>It is loosely-coupled architecture designed to meet the business needs of the organization.SOA is
logical way of organizing software systems to provide end users distributed over the network with
services through published and discoverable interface.<p>

  <p>The fundamental building block of service-oriented architecture is a service.
    It is defined as a program that can be interacted with through well-defined message exchanges
    and designed for both availability and stability. Services carry out function, such as
    producing data, validating a customer, or providing simple analytical services.</p>

<h3>Characteristics:-</h3>
<ol>
  <li>Evolutionary: SOA is an evolutionary concept, which has evolved from years.</li>
<li>It’s not a product or technology: It is a set of architectural principles independent of any
product.</li>
<li>It’s incremental: Service orientation can and should be an incremental process-one that can
often be done in-house.</li>
<li>SOA provides a strategic capability for integrating business processes, data, and
organizational knowledge.</li>
<li>Disadvantage of SOA is for applications with GUI functionality.</li>
<li>Though the principle is same, Service oriented architecture is not same as Web services,
which indicates amalgamation of technologies, such as SOAP and XML.</li>
<li>Integration with different platforms is possible, with exchange of XML messages.</li>
<li>Reuse of code is possible which decreases the effort required to develop.</li>
<li>Ease of construction and maintenance.</li>
</ol>

<p>One can consider cloud as the extension of SOA. Service orientation is the core reference
  model for cloud computing since both emphasize on the concept of services as main building
  block of application and system development.</p>

  <p>SOA and Cloud Computing can also exist separately—neither depends on the other.
    However, they are complementary to each other.</p>

<p>Because of the fact that the applications do not have a solid architectural foundation, without SOA
organizations will face difficulties in using services provided by Cloud Computing. Service oriented
computing supports the development of rapid, low cost, flexible and evolvable applications and
system. It introduces the important concept of Quality of service (QOS) and Software as a service
(SaaS) which is also foundation of cloud computing.</p>

<h2><u>Utility Computing</u></h2>
<p>It is a service provisioning model in which a service provider makes computing resources and
infrastructure management available to the customer on demand, and on the basis of pay per use model.</p>

<p>Utility computing is one of the most popular IT service model, primarily because of the
  flexibility and economy it provides. Utility is the packaging of computing resources, such as
  computation, storage and services, as a metered service. This model has the advantage of a low
  or no initial cost to acquire computer resources.</p>

<p>The model is based on the conventional utilities such as telephone services, electricity,
  water and gas. Like other types of on-demand computing (such as grid computing), the utility
  model seeks to maximize the efficient use of resources and minimize associated costs.</p>

<h3>Principle:-</h3>
<p>The consumer has access to a virtually unlimited supply of computing solutions over the
  Internet or a virtual private network, which can be sourced and used whenever needed on demand.
  Various billing models are also being explored since it is a pay per use model.</p>

<p>Common ones are: Billing per user count, Billing per Gigabyte, Billing per hour/day.</p>

<h3>Realization of utility computing:-</h3>
<p>Various factors contributed to the concept of utility computing. Such as- Capillary
  diffusion of Internet and Web provided technological means to realize utility computing
  through simple interface at world scale.</p>

<p>Service oriented Architecture introduced the concept of leveraging external service for performing
specific task</p>

<p>E-Commerce provided infrastructure support for utility computing.<p>
