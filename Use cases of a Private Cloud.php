              <p> Private cloud can be used in several ways. Following are a few use cases:</p>
              <ul>
                  <li><b>Marketing Teams-</b> When marketing teams needs to launch a new marketing campaign, they need a few servers such as web server, a collaboration server, a content management server, and a Customer Relationship Management (CRM) application server. These can be easily deployed through a private cloud.</li>
                  <li><b>Application Deployment-</b> The private cloud can be used to form a dedicated development environment to support a new application project. The environment can comprise a web server, database server, development server, staging server, testing server, and version control server.</li>
                  <li><b>Workgroup Applications-</b> Private cloud can be used to host workgroup applications such as collaboration, e-mails, instant messaging, blog servers, etc.</li>
                  <li><b>Enterprise Applications-</b> A private cloud can be used for CRM, HR, or Enterprise Resource Planning (ERP) applications.</li>
                  <li>Engineering Applications- A private cloud can be used for engineering applications such as finite element analysis, batch processing, and compute-intensive applications. The availability of a large pool of server resources makes it ideal for off-hour batch jobs.<br>

                      Although private clouds have all these benefits, they attract criticism. This is because the organization has to buy, build, and manage the infrastructure. The capital expenses to procure non-IT components for the datacenter and IT hardware and software and often prohibitive, especially for Small and Medium Enterprises (SMEs)
                      </li>
