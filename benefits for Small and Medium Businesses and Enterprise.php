              <center><u><h1>Benefits for Small and Medium Businesses and Enterprise</h1></u></center>
              
              <p>The popularity of cloud computing especially among Small and Medium Businesses (SMBs), is undeniable, and they use cloud computing for internal and mission-critical purposes.</p>
              
              <p>Large organizations use public cloud for less critical applications such as Disaster Recovery (DR) and backups. However, now, they are evaluating or using cloud computing for mission-critical services such as Customer-Relationship Management (CRM) and Enterprise Resource Planning (ERP). They are also motivated by the cloudâ€™s large-scale, on-demand resource availability and ease of use. </p>
              
              <p>The key benefits for SMBs and enterprises are as follows:</p>
              
              <ul>
              <li><b>Scalability-</b> It is the ability of the cloud service or application to grow or diminish the resources (CPU, RAM, bandwidth, storage) based on need. This is often done without human interventions.</li>
<li><b>Ease of Use- </b>A self-service portal makes it easy to configure and use cloud resources. If a new virtual machine is required for test, development, or production, it can be quickly setup and put to use in a matter of minutes, compared to on-premise procurement and configuration, which takes more than a week.</li>
<li><b>Reduced capital Expenses (CapEx)-</b>There is no upfront capital investment in the physical resources procurement, maintenance, upgrade, or administrative costs.</li>
<li><b>Risk Reduction-</b>SMBs and enterprise can use the cloud to build IT configurations to experiment with the new business ideas, technologies, and models, before making large-scale investments.</li>
<li><b>Pay-For-What-You-Use-</b> The organization is billed for what they have used during the month.</li>
<li><b>Lower Operating Expenses (OpEx) - </b>The cost for cloud resources and support manpower is shared by many customers and the utilization percentage is high. Thus, the consequential economics of scale help lower the cost for consumers.</li>
<li><b>Flexibility to Hire Talent-</b> A business can have employees spread across the world and have them work on a common cloud-based platform. This enables an organization to use the best talent available at the lower cost.</li>
<li><b>Collaboration-</b> Anytime-anywhere access enables employees and partners to work concurrently on common projects.</li>
<li><b>Assurance with Service Level Agreements (SLAs)-</b> The consumers or the business has an agreement with the provider for a certain level of uptime, performance and problem response, and resolution time.
