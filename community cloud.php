		  <center><h1><u>Community Cloud</u></h1></center>

              
              <p>A community cloud is an infrastructure used by a specific community of users or organizations. They are either working as a common project or have a set of requirements such as security or compliance. The NIST, in its document number SP800-145, defines a community cloud as <i>“a cloud infrastructure provisioned for exclusive use by a specific community of consumers from organizations that have shared concerns (for example, mission, security requirements, policy, and compliance considerations). It may be owned, managed, and operated by one or more of the organizations in the community, a third party, or a combination of some of them, and it may exist on or off premises.”</i></p>
              
              <p>A community cloud may contain the following features:</p>
              
              <ul>
                  <li>Like a private cloud, it may be managed by the users or by a third party.</li>
                  <li>It may be located within premises of a user organization or at a hosting or space provider’s site.</li>
              <li>It is more expensive than a public cloud, because the infrastructure and cost are shared by a few users or organizations.</li>
          <li>It offers a higher level of compliance, security, and privacy to the user group.</li>
          <li>Its service is designed around a particular set of contextual constraints for the purpose of supporting a specific and targeted user base. 
          <br>Those contextual constraints most often relate to security requirements (both physical and logical), compliance validation, audit requirements and certification.</li>
              </ul>
              
              <p>The community cloud strategy is emerging most strongly in heavily regulated verticals such as the following:</p>

              <ul>
                  <li>Healthcare</li>
                  <li>The federal government</li>
                  <li>Financial services</li>
                  <li>Interaction-heavy use cases</li>
              </ul>
              
              <p>As an example, consider the healthcare sector. The Health Insurance Portability and Accountability Act (HIPAA) cloud compliance has been challenging for security professionals in healthcare because of specific technical controls required by regulations for data privacy. It is also difficult to get cloud providers to understand the functions, which often becomes an educational exercise. Given these constraints, a healthcare organization might seek to take advantage of a community cloud. For example, it could use implementations that are purpose built to support HIPPA. In situation where interaction with peers is a requirement, such as Health Information Exchange (HIF), one participating organization in the community could set up a compliant environment and facilitate safe sharing of patient records and medical reports.</p>
              
              
              
