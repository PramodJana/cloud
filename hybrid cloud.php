              <p>Hybrid cloud concept mostly applies to IT resources rather than software services. Flexibility, control and security of deployment models are offered by hybrid cloud solutions.</p>
              
              <h2>Hybrid cloud characteristics: </h2>
              <ol>
                  <li><b>Optimal utilization:</b> Hybrid clouds can increase server utilization by scaling out to public resources to handle demands. </li> 
                  <li><b>Consolidation: </b>Server consolidation is allowed in hybrid cloud, resulting in operating costs reduction.</li>  
                  <li><b>Availability:</b> In a hybrid cloud environment, the public cloud can scale up or take over operations completely if the companyâ€™s data centre is unavailable due to failures or attacks.</li>  
                  <li>Critical activities are executed on the private cloud and the non-critical activities on public cloud.</li>
              </ol>
              
              
              <h2> Advantages:</h2>
              <ul>
                  <li>Scalability is more since it contains both private and public cloud.</li> 
                  <li>Because of designated private cloud highest level of security is offered by hybrid cloud.</li> 
                  <li>It can reduce and manage the cost based on the requirements. A primary advantage of a hybrid cloud model is that an organization only pays for extra compute resources when needed.</li> 
                  <li>For cloud bursting for scaling hybrid clouds are used by enterprise.</li>
              </ul>
              
              <h2> Disadvantages:</h2>
