             <center><h1><u>Hybrid Cloud</u></h1></center> 
              
              <p>Hybrid clouds are also known as heterogeneous cloud is the combination of Private and Public cloud system. It is a distributed system resulting from a Private Cloud that integrates services and resources from Public Clouds. They are bounded together by standardized or proprietary technology that enables data and application portability.  </p>
              
              <p>It employs beneficial aspects of involved cloud models and is the most common method of cloud deployment within a large organization.</p>
