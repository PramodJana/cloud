              <center><h1><u>Private Cloud</u></h1></center>
              
              <p>Private cloud is a cloud infrastructure build exclusively for an enterprise and deployed within the boundaries of firewall settings. It is managed internally or by a third-party and hosted internally or externally. Data stored in the private cloud can only be shared amongst users of and organization and third party sharing depends upon trust they build with them. It is also pay per use model. The private cloud is infrastructure that emulates all the features of cloud computing on a private network Popular examples of private cloud include Amazon Virtual Private Cloud (Amazon VPC), Eucalyptus Cloud Platform, IBM Smart Cloud Foundation and Microsoft Private Cloud.  </p>
              
              <h3>There are two variations of private clouds:  </h3>
              
              <p><b>Internal Cloud:</b>   It is also known as On-Premise Private Cloud. </p>
              
              <ol type="i">
                  <li>Hosted within an enterprise data centre.</li>
                  <li>It provides a more standardized process and protection.  </li>
                  <li>Limited in size and scalability.  </li>
                  <li>Complete control and configurability of the infrastructure and security. </li>
              </ol>
              
              <p><b>External cloud:</b> It is also known as Hosted Private Cloud.  </p>
              
              <ol type="i">
                  <li>Hosted by external cloud computing provider.</li>
                  <li>Full guarantee of privacy by service provider.</li>
              <li>Generally used in place of public cloud.</li>

              </ol>
              
              <h2>Characteristics of private cloud:-  </h2>
              <br>
              <center>
              <img src="img1/private%20cloud%20characteristics.png" class="img-fluid" alt="Responsive image">
			  </center>
              <br><br>
              
              <ol type="1">
                  <li><b>Enhanced Security Measures: </b>Virtual private cloud computing comes equipped with a customizable and thorough firewall and a plethora of security tools which ensure maximum protection against unauthorized use, hacking and other such malicious attempts.</li>
                    <li><b>Dedicated Resources:</b>  An enterprise has its own dedicated resources such as processor time and data buses .There is no compromise with resource availability and performance.  </li>
 
<li><b>Greater Customization: </b>In order to suit the exact requirements of an enterprise, private cloud services are acquiescent and customizable. This in turns bestows the enterprise with more control over their data.</li> 
</ol>
              
              <h2>Advantage: </h2>
              <p>Though private clouds are owned and used by a single organization, they offer many of the same benefits as public clouds. The benefits of private cloud are-  </p>
              
              <ul>
                  <li>Customization</li>
                  <li>Dedicated resources</li>
              <li>Enhanced security</li>
          <li>Greater flexibility and control to owner.</li>  
          <li>Ability to customize: Customer can make changes depending upon their requirements.</li> 
      <li>Resiliency: It has the ability to recover from disaster. </li>
    <li>Security and privacy: Due to hosting within the boundary, it offers more security and privacy to data. Security concerns are addressed through secure-access VPN or by the physical location within the client’s firewall system. </li>
    <li>Latency: Private clouds provide lower latency than public clouds during peak periods. </li>
              </ul>
              
              
              <h2>Disadvantage: </h2>
              <ul>
                  <li>This model doesn’t bring much in terms of cost efficiency. </li>
                  <li>For critical applications, there is need to consider downtime in terms of internet availability, quality and performance.</li> 
                  <li>Higher initial outlay.</li> 
                  <li>Remote access: It is also more difficult to access the data held in a private cloud from remote locations due to the increased security measures. </li>
                  <li>Underutilization: Resources are not properly utilized. </li>
                  <li>Platform Scaling: Scalability is less compare to public cloud.</li>
              </ul>
              
              
