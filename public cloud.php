              <center><u><h1>Public Cloud</h1></u></center>
              <p><i>The cloud infrastructure is provisioned for open use by the general public over a public network and is analogous to cloud computing.</i></p>
              
              <p><b> The model truly represents cloud hosting .It   is a cloud hosting environment in which the cloud services are delivered over a network open for general usage. The public cloud offers a set of standardized business process, application, and infrastructure services on a flexible and pay-as-you-go basis. A public cloud is owned and managed by a third-party service provider and access is on subscription.  </b></p>
              
              <h2>Principle:</h2>
              <p> Public cloud functions on the prime principle of:-</p>
              <ul>
                  <li>Storage.</li>
                  <li>Demand.</li>
                  <li>Scalability.</li>  
                  </ul>

              <p>Examples of public clouds include Amazon Elastic Cloud Compute, Google App Engine, Blue Cloud by IBM and Azure services Platform by Windows.  </p>
              
              <h2>Characteristics:</h2>
              <br><br>
			  <center>
               <img src="img1/Characteristics%20of%20Public%20Cloud.png" class="img-fluid" alt="Responsive image">
				</center>
			   <br>
              
              <ol>
                  <li><b>Flexible and Elastic Environment:</b> Public cloud is a highly flexible cloud environment. It offers user to share and store data as per their personal capacities. And also has the freedom to decide what to share and what not to share with their clients.<br>
                      Example: Google App engine or Amazon elastic cloud compute</li> 

                  <li><b>Self-Service:</b> Customer is independent to perform operations of their own such as creation, manipulation etc.</li> 
                  <li><b>Pay for what you use:</b> Payment is charged on the basis  services used by users. It brings economy of scale in pooling datacenter resources, virtualization and on-demand provisioning. The model helps to reduce capital expenditure and bring down operational costs. </li>

                  <li><b>Availability:</b> Public cloud services are available to all and at any time.</li> 

                  <li><b>Reliability:</b>  Reliability of service is there because data is replicated.</li>
              </ol>
            
              <h2>Advantages:</h2> 
