              <center><h1><u>Self-service features in a Cloud Deployment</u></h1></center>
              
              <p>Portal-based self-service is a critical component for cloud deployments. It is used by consumers to request cloud services and resources from the cloud infrastructure. These requests must be automated and be in real-time. The users must be able to immediately get what they request for, through the portal.</p>
              
              <p>Self-service feature can be used to improve performance by automating allocation of resources on the basis of application load. It can also be used to automate repetitive administrative tasks such as saving log files to a remote log server or customizing home directories for new users. In addition, this service must be used to enforce security and restrict authorization of users.</p>
              
              <p>Despite the benefits, there are some drawbacks to self-service. It gives you, the portal user, a lot of freedom. As such, users have the ability to do more tasks via the portal, but self-service may increases security threads. Some tasks may unnecessarily increase resource utilization fees or break compliance requirement. Using a self-service portal to move certain data to the cloud may not be the right action.</p>
              
              <p style="color:green">NOTE: While self-service in the cloud is a requirement, the access must be controlled. There needs to be a policy on what each cloud user is eligible to do via the cloud portal.</p>
              
              
              <h2><u>Federated Cloud Deployments</u></h2>
              
              <p>A federation is an integration of various smaller units, put together to accomplish a range of tasks. A federation of clouds is a mechanism used by a cloud provider, where it rents and integrates resources, applications, or services from various cloud vendors to meet extensive and large-scale customer needs.</p>
              
              <p>For consumers, a federated cloud offers many benefits. Various applications and unlimited resources are available from a single provider. The interaction between the multi-vendor services is tested by the providers. Since the services come from various providers, there is no vendor lock-in. the resources are limited via many providers and hence the utilization percentage is high, which helps reduce the cost for consumers. The performance is better due to caching and having multiple data copies at various provider locations. These copies can be accessed by users globally, who benefit from having copies close to them. Data availability within a federal cloud is better due to data being replicated to multiple sites.</p>
              
              <p>Security and compliance in a multi-vendor federated cloud is a primary concern. The data is physically present in various datacenters around the world.</p>
              
              <p style="color:green">NOTE: In a federated cloud, customers should make sure that all the cloud providers audit their environments and comply with the regulations. They must be aware of the security policies and practices followed by the providers and know the proposed terms in the SLA with the federated and other direct providers.</p>
              
              <p>Another concern in a federated cloud is the authentication of users and authorization to use various services. A federated cloud requires a robust identity management system. Besides authentication, the various applications from providers require different level of permission. There are various industry solution developed using SAML and other standards, which enable identity management applications to link users to applications from different service providers.</p>
              
              
              
              
              
              
              
              
              
              
              
              
              
                            
              
              
